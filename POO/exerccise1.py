class Student:
    def __init__(self, document, name, email, phone, age):
        self.document = document
        self.name = name
        self.email = email
        self.phone = phone
        self.age = age

    def getdocument(self):
        return self.document

    def getname(self):
        return self.name

    def getemail(self):
        return self.email

    def getphone(self):
        return self.phone

    def getage(self):
        return self.age

        
    