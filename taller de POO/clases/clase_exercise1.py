class Calculator:
    def __init__ (self,n1,n2):
        self.n1=n1
        self.n2=n2

    def Sum (self):
        return self.n1+self.n2    
    
    def rest (self):
        return self.n1-self.n2   
    
    def multiplicatiom (self):
        return self.n1*self.n2
    
    def division (self):
        return self.n1/self.n2   